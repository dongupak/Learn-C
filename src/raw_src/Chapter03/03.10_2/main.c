#include <stdio.h>

int main() {
    int x = 7;  // 0000 0111 (2진수)
    x &= 5;     // x = x & 5; (5 = 0000 0101)
    printf("x &= 5 : %d\n", x);  // 출력: 5 (0000 0101)

    x = 2;   // 0000 0010
    x |= 4;  // x = x | 4; (4 = 0000 0100)
    printf("x |= 4 : %d\n", x);  // 출력: 6 (0000 0110)

    x = 7;   // 0000 0111
    x ^= 5;  // x = x ^ 5; (5 = 0000 0101)
    printf("x ^= 5 : %d\n", x);  // 출력: 2 (0000 0010)
}